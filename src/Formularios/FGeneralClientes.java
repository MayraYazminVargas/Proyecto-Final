    private void llenarTodo() {
        //"IdCliente", "Nombre", "Apellido", "Correo Electronico", "Direcci√≥n"
        for (int i = 0; i < c.length; i++) {
            //to add new row: new Object[] {String,String String, float}                               
            table.insertRow(0, new Object[]{c[i].getId(), c[i].getNombre(),
                 c[i].getTelefono(), c[i].getCorreo(),
                 c[i].getDireccion()
            });

        }
    }

    public void actualizar() {
        if (table.getRowCount() > 0) {
            for (int i = 0; i < c.length; i++) {
                table.removeRow(0);
            }
        }
        c = ac.consultarClientes(orden);
        llenarTodo();
    }


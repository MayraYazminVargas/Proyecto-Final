    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        id=txtId.getText();        
        if(v.id(id)){            
            detalle=acv.consultarDetalladaas(id);
            if(detalle.getId().equals(id)){
                quitarTodo();
                llenarTodo();
                btnEliminar.setEnabled(true);
                btnCancelar.setEnabled(true);
                txtId.setText(id);
                
            }else{
                JOptionPane.showMessageDialog(null, "El ID ingresado no fue encontrado",
                    "ID no existente", JOptionPane.WARNING_MESSAGE);
                quitarTodo();
            }
        }else{
            JOptionPane.showMessageDialog(null, "El ID ingresado no es valido, debe tener una longitud de 5 caracteres",
                "ID no valido", JOptionPane.WARNING_MESSAGE);
            quitarTodo();
        }
    }//GEN-LAST:event_btnBuscarActionPerformed


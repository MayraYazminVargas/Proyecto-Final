    private void llenarTodo(){        
        VentasCompras[] c;                   
        String fecha = detalle.getDia()+"/"+detalle.getMes()+"/"+detalle.getAnio();
        txtId.setText(id);
        txtCliente.setText(detalle.getIdAquel());
        txtFecha.setText(fecha);
        txtTotal.setText("$"+detalle.getTotal());
        c = acv.consultarCompraVenta(id);                
        for(int i=0; i<acv.getNumproductos(); i++){                              
            table.insertRow(table.getRowCount(), new Object[] 
                { c[i].getIdP(), c[i].getCantidad() , c[i].getSubtotal()
            });                       
        }
    }
    
    private void quitarTodo(){        
        if(table.getRowCount()>0){
            for (int i = 0; i < acv.getNumproductos(); i++) {
                table.removeRow(0);
            }
        }        
        txtCliente.setText("");
        txtFecha.setText("");
        txtId.setText("");
        txtTotal.setText("");
        btnBuscar.setEnabled(true);
        btnEliminar.setEnabled(false);
        btnCancelar.setEnabled(false);
    }

